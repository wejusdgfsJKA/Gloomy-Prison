public static class PlayerSettings
{
    /// <summary>
    /// How fast will the player's camera rotate vertically.
    /// </summary>
    public static float xSens { get; set; }
    /// <summary>
    /// How fast will the player's camera rotate horizontally.
    /// </summary>
    public static float ySens { get; set; }
    /// <summary>
    /// How close the player must be to an interactable in order to use it?
    /// </summary>
    public static float interactionDistance { get; set; }
}
